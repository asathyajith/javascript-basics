// Arrays — use numbered indexes.

const cars = ["Saab", "Volvo", "BMW"];

// Common methods to know:
// length, push, pop, shift, unshift, join, slice, splice, sort, reverse, indexOf, includes
console.log(cars.length);          // 3
console.log(cars.join(", "));      // "Saab, Volvo, BMW"
console.log(cars.slice(0, 2));     // ["Saab", "Volvo"]

